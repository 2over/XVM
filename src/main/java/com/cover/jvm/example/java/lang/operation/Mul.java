package com.cover.jvm.example.java.lang.operation;


public class Mul {

    public static void main(String[] args) {
        mulInt();
        mulFloat();
        mulLong();
        mulDouble();
    }

    public static void mulInt() {
        int a = 1;
        int b = 2;

        System.out.println(a * b);
    }

    public static void mulFloat() {
        float a = 1;
        float b = 2;

        System.out.println(a * b);
    }

    public static void mulLong() {
        long a = 1;
        long b = 2;

        System.out.println(a * b);
    }

    public static void mulDouble() {
        double a = 1;
        double b = 2;

        System.out.println(a * b);
    }

}
